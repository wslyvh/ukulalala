interface Props {
  scale: "A" | "B" | "C" | "D" | "E" | "F" | "G";
}

export function StandardScales(props: Props) {
  // https://liveukulele.com/scales/major/
  if (props.scale === "A") {
    return `
        A |-0-2-4-5-7-9-11-12-11-9-7-5-4-2-0-|
        E |----------------------------------|
        C |----------------------------------|
        G |----------------------------------|`
  }
  if (props.scale === "B") {
    return `
        A |-2-4-6-7-9-11-13-14-13-11-9-7-6-4-2-|
        E |------------------------------------|
        C |------------------------------------|
        G |------------------------------------|`
  }
  if (props.scale === "C") {
    // https://www.youtube.com/watch?v=daatI0t2vEE
    return `
        A|-----------0-2-3-2-0-----------|
        E|-----0-1-3-----------3-1-0-----|
        C|-0-2-----------------------2-0-|
        G|-------------------------------|`
  }
  if (props.scale === "D") {
    // https://www.youtube.com/watch?v=43nyjQfE2lM
    return `
        A|---------0-2-4-5-4-2-0--------|
        E|---0-2-3---------------3-2-0--|
        C|-2--------------------------2-|
        G|------------------------------|`
  }
  if (props.scale === "E") {
    return `
        A |-------0-2-4-6-7-6-4-2-0-------|
        E |-0-2-4-------------------4-2-0-|
        C |-------------------------------|
        G |-------------------------------|`
  }
  if (props.scale === "F") {
    // https://www.youtube.com/watch?v=WcdsquNo-Lw
    return `
        A |-----0-1-3-5-7-8-7-5-3-1-0-----|
        E |-1-3-----------------------3-1-|
        C |-------------------------------|
        G |-------------------------------|`
  }
  if (props.scale === "G") {
    return `
        A |---0-2-3-5-7-9-10-9-7-5-3-2-0---|
        E |-3----------------------------3-|
        C |--------------------------------|
        G |--------------------------------|`
  }
}

export function MajorScales(props: Props) {
  // https://ukuleletabs.org/scales/major/
  if (props.scale === "A") {
    return `
        A |--------------9-11-12-11-9--------------|
        E |------9-10-12--------------12-10-9------|
        C |-9-11------------------------------11-9-|
        G |----------------------------------------|`
  }
  if (props.scale === "B") {
    return `
        A |------------9-11-13-14-13-11-9------------|
        E |----9-11-12--------------------12-11-9----|
        C |-11------------------------------------11-|
        G |------------------------------------------|`
  }
  if (props.scale === "C") {
    return `
        A |-----------0-2-3-2-0-----------|
        E |-----0-1-3-----------3-1-0-----|
        C |-0-2-----------------------2-0-|
        G |-------------------------------|`
  }
  if (props.scale === "D") {
    return `
        A |-----------2-4-5-4-2----------|
        E |-----2-3-5-----------5-3-2----|
        C |-2-4----------------------4-2-|
        G |------------------------------|`
  }
  if (props.scale === "E") {
    return `
        A |-----------4-6-7-6-4-----------|
        E |-----4-5-7-----------7-5-4-----|
        C |-4-6-----------------------6-4-|
        G |-------------------------------|`
  }
  if (props.scale === "F") {
    return `
        A |-----------5-7-8-7-5-----------|
        E |-----5-6-8-----------8-6-5-----|
        C |-5-7-----------------------7-5-|
        G |-------------------------------|`
  }
  if (props.scale === "G") {
    return `
        A |------------7-9-10-9-7------------|
        E |-----7-8-10------------10-8-7-----|
        C |-7-9--------------------------9-7-|
        G |----------------------------------|`
  }
}
